interface HeroStatsTableDisplay {
    HeroName: string;
    PrimaryAttribute: { [key: number]: StatTable };
    AttackAttribute: { [key: number]: StatTable };
    DefenseAttribute: { [key: number]: StatTable };
    OtherStats: { [key: number]: StatTable };
    Player: { [key: number]: PlayerDataDisplay };
}

interface PlayerDataDisplay {
    CountPoint: number;
    GoldCollectionProgress: number;
    AutofillButtonIsEnabled: boolean;
    CostPoint: number;
    IncreasePricPerPoint: number;
    MaxCostPoint: number;
}

interface StatTable {
    StatName: string;
    StatImage: string;
    StatCount: number;
    StatLimit?: number;
    StatPerPoint?: number;
    IsImproved?: boolean;
}

interface HeroStatsTable {
    [key: number]: { [key: string]: BonusStatTable };
}

interface BonusStatTable {
    ["StatCount"]: number;
}
