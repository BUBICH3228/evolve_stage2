interface ForestBossTable {
    Targets: { [key: string]: BossDataTable };
}

interface BossDataTable {
    vSpawnLoc?: Vector;
    vSpawnVector?: Vector;
    goldPerKill: number;
    XPPerKill: number;
    unlockLocationID: number;
    locationID: number;
    Death: number;
}

interface ForestSpawnerTable {
    Locations: { [key: number]: LocationTable };
}

interface LocationTable {
    spawnRadius: number;
    findUnitsRadius: number;
    respawnTime: number;
    pointName: string;
    countCamps: number;
    locationOpen: boolean;
    numberlocation: number;
    locationLevel: number;
    locationPoints: { [key: number]: Vector };
    locationVectors: { [key: number]: Vector };
    camps: { [key: number]: CampsTable };
}

export interface CampsTable {
    creeps: { [key: number]: CreepTable };
}

export interface CreepTable {
    creepName: string;
}

export interface LineCreepSettings {
    waweInterval: number;
    bossWaweInterval: number;
    shiftWaveInterval: number;
    currentShift: number;
    currentWave: number;
    wavesTable: { [key: number]: WaveInfo };
    bossesTable: { [key: number]: WaveInfo };
    unitModel: { [key: number]: string };
    spawnPoint?: CBaseEntity;
    IsSpawnerEnabled: boolean;
}

export interface WaveInfo {
    units: { [key: number]: string };
}

export interface UnitTable {
    unitName: string;
}
